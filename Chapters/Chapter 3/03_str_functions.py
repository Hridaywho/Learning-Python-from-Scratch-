# len () Function  

str = "Hriday"
print(len(str))  #Prints the length of the string or variable a

#string.endswith("iday") Function 
str = "Hriday"
print(str.endswith("iday"))  #Checks if the string or variable str ends with "iday"
print(str.startswith("Hri"))  #Checks if the string or variable str starts with "Hri"

#String.count("c")Function  
str = "harry"
print(str.count("r"))  #Counts the occurrences of "r" in the string or variable str

#First Character capitalization of a String 
str = "Hriday"
capitalized_string = str.capitalize()  #Capitalizes the first character of the string or variable str
print(capitalized_string)  #Prints the capitalized string

#First Character of a String               
str = "Hriday"
print(str[0])  #Prints the first character of the string or variable str

#String.findword (word)
s = "Hello world"
index = s.find ("world")  #Finds the index of the word "world" in the string s
print(index)  #Prints the index of the word "world"

#String.replace("old word", "new word") Function
str = "harry"
replaced_string = str.replace("r", "l")  #Replaces all occurrences of "r" with "l" in the string or variable str
